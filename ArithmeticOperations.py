intVariable = 7
floatVariable = 6.3
print(intVariable)
print(floatVariable)


#                      7 + 6.3
plusResult = intVariable + floatVariable
print(plusResult)

#                             7 - 6.3
subtractionResult = intVariable - floatVariable
print(subtractionResult)

#                       7 / 2
divisionResult = intVariable / 2
print(divisionResult)


#                               7 * 3
multiplicationResult = intVariable * 3
print(multiplicationResult)

modulusResult = 7 % 2 #7/2 -> 3 + 1/2 (1 over the divisor, 1 is remainder)
print(modulusResult)

powerResult = intVariable**2 #7^2 
print(powerResult)

